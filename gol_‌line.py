def walkAllInternalSpots():
    for x in range(len(worldarray)):
        for y in range(len(worldarray)):
            #print (" x is ", x , "y is ", y)
            if x==0:
                continue
            elif y==0:
                continue
            elif x == len(worldarray)-1:
              continue
            elif y==len(worldarray)-1:
              continue
            else:
              findNeighbors(x, y)

